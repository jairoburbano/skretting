    <p>
        Una de estas experiencias productivas, tuvo lugar en Abril del presente año, con el ingreso de 624.839 smolt de 131 gr de peso promedio a un centro de engorda ubicado en la Región de Los Lagos. Estos smolt fueron alimentados por 6 semanas en agua dulce con Nutra Supreme. El transporte fue efectuado desde una piscicultura ubicada en la Región de Aysén (XI). La distribución de las siembras se desglosó en aproximadamente 50.000 smolt por unidad de cultivo en jaulas metálicas de 30x30 metros y alimentados en forma automática por 6 semanas con la dieta post transferencia Spirit Supreme.
    </p>
    <p>
        Para el análisis de los datos, se realizó seguimiento de los smolt durante 6 semanas post ingreso y se consolidó la información individual diaria de cada jaula en términos de consumo de alimento. Los datos diarios fueron corregidos utilizando el modelo de crecimiento de Skretting de Agua de Mar, en relación al máximo consumo de alimento esperado para el peso, temperatura y época del año. De esta forma, fue posible llevar todas las unidades de cultivo a una misma fecha de ingreso, para hacerlas comparables en términos de fecha de ingreso al centro de engorda.
